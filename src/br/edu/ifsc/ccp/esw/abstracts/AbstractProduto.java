package br.edu.ifsc.ccp.esw.abstracts;

/**
 *
 * @author Aluno
 */
public abstract class AbstractProduto {
    
    protected String codigo;
    protected String descricao;
    protected String autor;
    protected int quantidadeEstoque;
    protected double preco;

    public AbstractProduto() {
    }

    public AbstractProduto(String codigo, String descricao, String autor, int quantidadeEstoque, double preco) {
        this.codigo = codigo;
        this.descricao = descricao;
        this.autor = autor;
        this.quantidadeEstoque = quantidadeEstoque;
        this.preco = preco;
    }

    
    
    /**
     * @return the codigo
     */
    public String getCodigo() {
        return codigo;
    }

    /**
     * @param codigo the codigo to set
     */
    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    /**
     * @return the descricao
     */
    public String getDescricao() {
        return descricao;
    }

    /**
     * @param descricao the descricao to set
     */
    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

    /**
     * @return the autor
     */
    public String getAutor() {
        return autor;
    }

    /**
     * @param autor the autor to set
     */
    public void setAutor(String autor) {
        this.autor = autor;
    }

    /**
     * @return the preco
     */
    public double getPreco() {
        return preco;
    }

    /**
     * @param preco the preco to set
     */
    public void setPreco(double preco) {
        this.preco = preco;
    }
    
    
    /**
     * @return the quantidadeEstoque
     */
    public int getQuantidadeEstoque() {
        return quantidadeEstoque;
    }

    /**
     * @param quantidadeEstoque the quantidadeEstoque to set
     */
    public void setQuantidadeEstoque(int quantidadeEstoque) {
        this.quantidadeEstoque = quantidadeEstoque;
    }
    
    public abstract void exibirInfo();
    
    
}
